#include "calc.h"

int Calculator::Addition(int num1, int num2)
{
	return num1 + num2;
}
int Calculator::Division(int num1, int num2)
{
	return num1 / num2;
}
 
int Calculator::Multiplication(int num1, int num2)
{
	return num1 * num2;
}

int Calculator::Subtraction(int num1, int num2)
{
	return num1 / num2;
}

